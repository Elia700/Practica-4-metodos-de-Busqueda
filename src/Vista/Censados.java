    private void CargarTabla() {
        
        mtp.setCensos(censoControl.getCensos());
        cbxEstado.setSelectedIndex(0);
        cbxEstadoEmocional.setSelectedIndex(-1);
        cbxCensadores.setSelectedIndex(-1);
        cbxMotivoDivorcio.setSelectedIndex(0);
        tblMostrar.setModel((TableModel) mtp);
        tblMostrar.updateUI();
    }
    

    private void Limpiar() {
        try {
            UtilVista.CargarComboRolesL(cbxEstadoEmocional);
            UtilVista1.cargarcomboRolesL(cbxCensadores);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        txtNombreCompleto.setEnabled(true);
        tblMostrar.clearSelection();
        txtFecha.setText(" ");
        txtNombreCompleto.setText(" ");
        CargarTabla();
        cbxEstado.setSelectedIndex(-1);
        cbxMotivoDivorcio.setSelectedIndex(-1);
        cbxEstadoEmocional.setSelectedIndex(-1);
        censoControl.setCensos1(null);
    }

    private Boolean Validar() {
        return (!txtNombreCompleto.getText().trim().isEmpty()
                && !txtFecha.getText().trim().isEmpty());
    }

    private void Guardar() {
        if (Validar()) {
             censoControl.getCensos1().setNombreCp(txtNombreCompleto.getText());
            censoControl.getCensos1().setEstado_censo(cbxEstado.getSelectedItem().toString());
            censoControl.getCensos1().setFecha(txtFecha.getText());
            censoControl.getCensos1().setMotivo_divorcio(cbxMotivoDivorcio.getSelectedItem().toString());
            censoControl.getCensos1().setId_censo(UtilVista.ObtenerCensoControladorL(cbxEstadoEmocional));
            censoControl.getCensos1().setCensador(UtilVista1.obtenerRolControl(cbxCensadores));

            if (censoControl.persist()) {
                JOptionPane.showMessageDialog(null, "Datos guardados con exito");
                censoControl.setCensos1(null);
                CargarTabla();
                Limpiar();

            } else {
                JOptionPane.showMessageDialog(null, "No se pudo guardar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falta llenar campos  ");

        }
    }
    //

    private void cargarVista() {
        int fila = tblMostrar.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Escoja un registtro de la tabla");
        } else {
            try {
                censoControl.setCensos1(mtp.getCensos().getInfo(fila));
                txtFecha.setText(censoControl.getCensos1().getFecha());
                txtNombreCompleto.setText(censoControl.getCensos1().getNombreCp());
                cbxEstado.setSelectedIndex(censoControl.getCensos().getEstado()-1);
                cbxCensadores.setSelectedIndex(censoControl.getCensos().getCensadores()-1);
                cbxMotivoDivorcio.setSelectedIndex(censoControl.getCensos().getMotivo()-1);
                cbxEstadoEmocional.setSelectedIndex(censoControl.getCensos().getId_censo1()-1);

            } catch (Exception e) {
               
            }

        }
    }

