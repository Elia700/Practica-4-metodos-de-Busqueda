    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (Validar()) {
            censoControl.getCensos1().setNombreCp(txtNombreCompleto.getText());
            censoControl.getCensos1().setEstado_censo(cbxEstado.getSelectedItem().toString());
            censoControl.getCensos1().setFecha(txtFecha.getText());
            censoControl.getCensos1().setMotivo_divorcio(cbxMotivoDivorcio.getSelectedItem().toString());
            censoControl.getCensos1().setId_censo(UtilVista.ObtenerCensoControladorL(cbxEstadoEmocional));
            censoControl.getCensos1().setCensador(UtilVista1.obtenerRolControl(cbxCensadores));
            
            if (censoControl.merge(censoControl.getCensos1(), tblMostrar.getSelectedRow())) {
                JOptionPane.showMessageDialog(null, "Datos guardados con exito");
                censoControl.setCensos1(null);
                CargarTabla();
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo guardar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falta llenar campos ");

        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnCorregirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCorregirActionPerformed

        int filaSeleccionada = tblMostrar.getSelectedRow();
        if (filaSeleccionada >= 0) {
            String nombreCp = mtp.getValueAt(filaSeleccionada, 0).toString();
            String fecha = mtp.getValueAt(filaSeleccionada, 1).toString();
            String estadoCenso = mtp.getValueAt(filaSeleccionada, 2).toString();
            String tipoCenso = mtp.getValueAt(filaSeleccionada, 3).toString();
            String motivo_divorcio = mtp.getValueAt(filaSeleccionada, 4).toString();
            String censadores= mtp.getValueAt(filaSeleccionada, 5).toString();
            txtNombreCompleto.setText(nombreCp);
            txtFecha.setText(fecha);
            cbxEstado.setSelectedItem(estadoCenso);
            cbxEstadoEmocional.setSelectedItem(tipoCenso);
            cbxMotivoDivorcio.setSelectedItem(motivo_divorcio);
            cbxCensadores.setSelectedItem(censadores);
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una fila para modificar.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCorregirActionPerformed

